func TestIsPrivate(t *testing.T) {
	// success - true
	ok := IsPrivate("__test__")
	assert.True(t, ok)

	// success - false
	for _, name := range []string{"__test", "test__", "test"} {
		ok := IsPrivate(name)
		assert.False(t, ok)
	}
}

